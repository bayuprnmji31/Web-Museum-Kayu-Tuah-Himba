                     <p>Rumah Lamin adalah rumah adat suku Dayak yang terletak di Kalimantan. Rumah ini memiliki peranan penting dalam kehidupan tradisional suku Dayak, baik sebagai tempat tinggal maupun sebagai pusat kegiatan sosial dan budaya. Suku Dayak, yang merupakan salah satu kelompok etnis tertua di Kalimantan, mulai membangun rumah Lamin setelah mereka beralih dari gaya hidup nomaden sebagai pemburu-pengumpul ke kehidupan yang lebih menetap. Rumah Lamin mencerminkan adaptasi mereka terhadap lingkungan sekitar dan berfungsi sebagai tempat tinggal bersama, tempat berkumpul, dan pusat upacara adat.</p>
                     <p>Dibangun dari bahan-bahan alami seperti kayu ulin yang tahan lama dan daun nipah atau rumbia sebagai atap, rumah Lamin didesain berbentuk panggung dengan ketinggian 1 hingga 2 meter dari tanah. Desain ini bertujuan untuk melindungi penghuninya dari banjir dan binatang buas, serta untuk meningkatkan sirkulasi udara agar rumah tetap sejuk. Selain fungsinya yang praktis, rumah Lamin juga dihiasi dengan ukiran dan motif tradisional Dayak yang melambangkan spiritualitas dan hubungan dengan alam.</p>
                     <p>Rumah Lamin tidak hanya berfungsi sebagai tempat tinggal, tetapi juga sebagai pusat komunitas di mana anggota suku berkumpul, berdiskusi, dan mengadakan berbagai upacara adat, seperti upacara kelahiran, pernikahan, dan kematian. Selain itu, rumah ini juga menjadi tempat pendidikan tradisional, di mana nilai-nilai budaya, pengetahuan, dan keterampilan diajarkan kepada generasi muda oleh para tetua dan anggota komunitas lainnya.</p>
                     <p>Rumah Lamin adalah struktur tradisional suku Dayak di Kalimantan yang berfungsi sebagai tempat tinggal dan pusat aktivitas sosial dan budaya. Suku Dayak, salah satu kelompok etnis tertua di Kalimantan, mulai membangun rumah Lamin setelah beralih dari gaya hidup nomaden ke kehidupan yang menetap. Rumah Lamin dibangun dari kayu ulin dan daun nipah atau rumbia, dengan desain panggung setinggi 1 hingga 2 meter untuk melindungi penghuni dari banjir dan binatang buas. Rumah ini dihiasi dengan ukiran dan motif tradisional yang melambangkan spiritualitas dan hubungan dengan alam.</p>
                     <p>Selain sebagai tempat tinggal, rumah Lamin adalah pusat komunitas di mana suku Dayak berkumpul untuk berdiskusi dan mengadakan upacara adat seperti kelahiran, pernikahan, dan kematian. Rumah ini juga menjadi tempat pendidikan tradisional di mana nilai-nilai budaya dan keterampilan diajarkan kepada generasi muda. Namun, modernisasi telah menyebabkan banyak orang Dayak meninggalkan rumah Lamin dan beralih ke rumah modern yang lebih praktis. Meskipun begitu, upaya pelestarian tetap dilakukan, termasuk menjadikan rumah Lamin sebagai objek wisata budaya. Rumah Lamin adalah simbol kekayaan budaya dan kearifan lokal suku Dayak, dan pelestariannya penting untuk menjaga keberagaman budaya Indonesia.</p> </div>
                     <p>Rumah Lamin adalah rumah adat yang terutama ditempati oleh suku Dayak di Kalimantan Timur, terutama oleh sub-suku Dayak Kenyah dan Dayak Kayan. Kedua sub-suku ini terkenal dengan arsitektur tradisional rumah Lamin yang megah dan berfungsi sebagai pusat kehidupan sosial, budaya, dan adat mereka.
                        <br>
                        Dayak Kenyah,
                        Suku Dayak Kenyah menempati wilayah hulu sungai di Kalimantan Timur dan Kalimantan Utara. Rumah Lamin bagi mereka adalah pusat komunitas di mana beberapa keluarga tinggal bersama. Rumah ini juga menjadi tempat untuk menjalankan upacara adat, pertemuan, dan kegiatan sosial lainnya.
                        <br>
                        Dayak Kayan,
                        Suku Dayak Kayan juga mendiami wilayah yang sama di Kalimantan Timur dan Kalimantan Utara. Mereka memiliki rumah Lamin yang serupa dengan Dayak Kenyah, dengan ciri khas ukiran dan motif-motif tradisional yang kaya akan simbolisme budaya dan spiritual.</p>                 
