                  <div class="page-content">
                     <p>Istilah “Punan” lebih dipandang sebagai sebutan
                        umum untuk kelompok masyarakat pemburu-peramu
                        yang dulu hidup secara berpindah-pindah di hutan
                        Kalimantan Timur, lalu Dayak menjadi sebutan untuk
                        masyarakat petani ladang. Kelompok Suku Punan
                        hingga sekarang banyak tinggal di bagian hulu sungai
                        bahkan di tengah hutan yang sangat terpencil. Pada
                        masa lalu, pemerintah telah melabelkan mereka sebagai
                        suku terasing yang hidup berpindah-pindah di dalam
                        hutan dan tidak punya tempat tinggal menetap, sama
                        halnya dengan beberapa suku lain seperti suku Kubu
                        di Sumatera. Namun seiring dengan perkembangan
                        pembangunan dan arus modernisasi telah terjadi
                        dinamika sosial budaya suku Punan.</p>
                     <p>Punan tinggal di dalam hutan yang dulu masih utuh
                        dan kaya sumberdaya alam. Kuhn dkk. (2000) telah
                        memvisualisasikan potret kehidupan Punan di Metut,
                        Kalimantan Timur. Sistem budaya suku Punan yang
                        bersifat terbuka sebagaimana umumnya masyarakat
                        sekitar hutan lainnya di Kalimantan secara lambat-laun
                        telah mengalami pergeseran yang berdampak pada
                        perubahan pola hidup. Mereka yang dulu hidup
                        berpindah-pindah di tengah hutan sebagai
                        pengumpul dan peramu, sejak akhir abad XIX mulai
                        bermukim di dusun-dusun kecil, dan awal abad XX
                        mulai melakukan perladangan sebagaimana suku
                        Dayak di sekitarnya dengan budidaya tanaman pangan
                        seperti padi dan ubi-ubian. Namun disamping
                        berladang, aktifitas berburu, menangkap ikan, dan
                        mengumpulkan hasil hutan masih tetap sebagai
                        aktifitas utama dalam kehidupan sehari-hari.</p>
                     <p>Suku Punan yang tinggal di sekitar hutan mempunyai
                        ketergantungan hidup pada sumberdaya hutan, kayu
                        dan bukan kayu sebagai sumber kehidupan, baik untuk
                        kebutuhan subsisten seperti sumber makanan, obat-obatan, tempat tinggal (bahan rumah), ritual budaya
                        (Uluk dkk. 2001). Lebih dari subsisten, hasil hutan
                        menjadi andalan utama kebutuhan komersial sebagai
                        sumber uang untuk kebutuhan yang terus meningkat.
                        Untuk mendapatkan kehidupan yang lebih baik,
                        mereka juga banyak merantau ke “Negeri Jiran”
                        Malaysia mencari kerja sebagai buruh di perusahaan
                        perkebunan sawit atau penebang pohon.</p>
                     <p>Rumah adat Dayak Punan, yang dikenal sebagai Lamin, merupakan bagian integral dari kehidupan tradisional suku Dayak Punan di Kalimantan. Rumah ini mencerminkan adaptasi dan hubungan erat mereka dengan alam, serta mencerminkan budaya dan tradisi mereka yang kaya. Suku Dayak Punan, salah satu kelompok etnis tertua di Kalimantan, awalnya hidup sebagai pemburu-pengumpul yang berpindah-pindah sebelum akhirnya menetap dan membangun rumah adat mereka. Lamin berfungsi sebagai tempat tinggal, pusat aktivitas sosial, dan pusat budaya, yang sangat penting dalam menjaga kohesi sosial dan identitas budaya suku Dayak Punan. Dibangun dari bahan alami seperti kayu ulin dan daun rumbia atau nipah, rumah ini memiliki desain panggung setinggi 1 hingga 2 meter untuk melindungi penghuni dari binatang liar dan banjir. Rumah adat ini juga dihiasi dengan ukiran dan motif tradisional yang melambangkan keyakinan spiritual dan hubungan dengan alam. Lamin berfungsi sebagai pusat komunitas, tempat di mana anggota suku berkumpul, mengadakan pertemuan, dan menjalankan upacara adat seperti kelahiran, pernikahan, dan kematian. Selain itu, rumah ini menjadi tempat pendidikan tradisional, di mana nilai-nilai budaya dan keterampilan diajarkan kepada anak-anak.</p>
