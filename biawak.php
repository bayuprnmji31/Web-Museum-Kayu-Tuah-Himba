                     <h2>Biawak Kalimantan</h2>
                     <p>Biawak kalimantan atau juga disebut sebagai Biawak tanpa telinga (Lanthanotus borneensis) merupakan satwa endemik di Kalimantan. Satwa ini pertama kali ditemukan di wilayah Sarawak bagian Utara, Malaysia pada tahun 1878. Hingga kemudian ditemukan di area perkebunan sawit, Kabupaten Landak, Kalimantan Barat pada tahun 2008, dan Kalimantan Timur pada tahun 2013.
                        Satwa ini merupakan satu-satunya jenis dari keluarga Lanthanotidae yang masih bertahan hingga kini. Jenis ini memiliki hubungan kekerabatan dekat dengan keluarga Varanidae (Varanus sp.). Meski bukan berasal dari keluarga Biawak, namun jenis ini secara umum dikenal sebagai Biawak.</p>
                     <p>Biawak kalimantan memiliki ciri yang unik karena menyerupai makhluk mitos Naga. Satwa ini tidak memiliki organ telinga (selaput timpanum) seperti jenis biawak lainnya, dan pola tubuh bergerigi. Biawak ini memiliki warna mata biru transparan yang berukuran kecil, dengan kelopak mata yang tebal. Bagian dada dan perut berwarna putih dengan pola marmer kecoklatan. Ukuran tubuh biawak ini relatif kecil dengan panjang tubuh antara 45 – 55 cm dan berat badannya mencapai 209.3 gram.
                        Biawak ini mendiami hutan tropis dataran rendah dan ditemukan di aliran sungai jernih berpasir dikelilingi oleh vegetasi hutan yang bervariasi. Satwa ini bersifat semiakuatik, hidup di dekat air dan cenderung menghindar ke dalam air ketika terancam. Biawak ini bersembunyi di dalam tanah dan melakukan kegiatannya di malam hari (nocturnal).
                        Hanya beberapa museum zoologi di dunia yang menyimpan spesimen biawak ini, yaitu di Inggris, Amerika, Singapura dan Malaysia. Di Indonesia, spesimen Biawak ini disimpan di Museum Zoologicum Bogoriense (MZB) di Cibinong, Bogor yang berasal dari hasil penyitaan kepolisian terkait penyelundupan Biawak kalimantan oleh WNA asal Jerman, Holger Pelz, di Bandara Soekarno Hatta, Tangerang, Banten pada Oktober 2015.
                        Di Eropa, Biawak ini termasuk salah satu binatang peliharan eksotis dengan harga selangit. dalam artikel dari the guardian “Lizard traffickers exploit legal loopholes to trade at world’s biggest fair“, disebutkan bahwa satu pasang satwa ini dijual seharga kira-kira Rp. 75 juta (€5000) saat pameran reptil terbesar di Eropa pada tahun 2015.</p>
                     <h2>Kucing Batu Kalimantan</h2>
                     <p>Kucing batu atau disebut marbled cat (pardofelis marmorata) merupakan satwa yang dilindungi. Kucing ini biasanya banyak ditemui di hutan-hutan yang ada di Indonesia salah satunya yakni Kalimantan. Kucing ini merupakan satwa asli kawasan timur Himalaya hingga Asia Tenggara. Satwa ini juga dijumpai di Indonesia. Kucing Batu merupakan hewan nokturnal atau aktif pada malam hari dan soliter (penyendiri). Hewan ini diperkirakan banyak menghabiskan waktu di atas pohon, hal inilah yang mengakibatkan keberadaannya jarang terlihat. Kucing ini hampir mirip dengan macan dahan, Kesamaan itu yakni dari tampilan yang memiliki pola totol yang khas dan tidak beraturan di bulunya. 
                     <p>Hewan ini memiliki totol berwarna hitam di bagian kakinya dan beberapa garis berwarna hitam di bagian kepala dan lehar, hal inilah yang membuatnya mirip dengan macan dahan.   Kucing Batu memiliki bobot kurang lebih sama dengan kucing domestik yang berukuran besar, namun dengan kesan tubuh yang lebih langsing dan panjang. Karena bentuknya tersebut, kucing ini dikenal sebagai hewan yang gesit. Kucing Batu memiliki ekor panjang dan berbulu lebat, sebagai bentuk adaptasi yang baik sesuai hidupnya di pepohonan. 
                     <p>Berbagai jenis kucing akan menampakkan ekornya dalam berbagai posisi, ekor kucing besar biasanya menggantung ke bawah, sedangkan spesies lain seperti kucing emas atau macan dahan dalam posisi tidak sedang berburu akan menaikkan ekornya. 
                        Namun, ekor kucing batu tampak mendatar ketika mereka berjalan. Hal ini sama seperti kebiasaan macan dahan. Hewan ini mampu bertahan hidup hingga berumur 8-15 tahun dengan berat 2-5 kg. Selain itu, kucing ini sangat bergantung pada kondisi alamnya yaitu hutan hujan tropis, sehingga Kucing Batu sangat peka dengan perubahan pada habitatnya.
                        Namun, keberadaan Kucing Batu saat ini terancam karena banyaknya penebangan hutan liar dan kebakaran hutan. Selain itu, kucing ini juga banyak diburu para pemburu liar. Pemburu liar selain memburu kucing ini untuk dijadikan hewan peliharaan juga mengambil kulit Kucing Batu untuk dijadikan sebagai hiasan karena polanya yang indah.
                     </p>
