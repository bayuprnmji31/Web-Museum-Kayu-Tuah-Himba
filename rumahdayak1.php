                  <div class="page-content">
                     <p>Suku Kayan Bahau atau Kayan Mekam adalah sebuah sub-suku dari suku Dayak Kayan yang sebagian besar mendiami kawasan Kabupaten Mahakam Ulu dan sebagian kecil berada di Kabupaten Kutai Barat, Kalimantan Timur.Suku ini mendiami Kecamatan Long Pahangai, Long Bagun,Long Hubung dan Laham di Kabupaten Mahakam Ulu dan Long Iram, Tering, sebagian Linggang Bigung dan Melak dan Barong Tongkok di Kabupaten Kutai Barat. Suku Dayak Bahau dibagi menjadi tiga sub-kelompok yaitu Bahau Modang, Bahau Busang, dan Bahau Saq. Suku Dayak Bahau umumnya tinggal di pinggiran sungai. Rumah-rumah berjejeran di sepanjang sungai. Populasi Dayak Bahau juga tersebar di kawasan Kecamatan Muara Wahau, Kecamatan Busang di Kutai Timur dan sebagian Kecamatan Tabang di Kutai Kartanegara.</p>
                     <p>Rumah adat Dayak Bahau, yang dikenal sebagai Lamin atau Umaq Daru, merupakan arsitektur tradisional suku Dayak Bahau di Kalimantan Timur, Indonesia. Rumah ini telah ada sejak zaman nenek moyang mereka dan berfungsi sebagai tempat tinggal bersama, pusat aktivitas sosial, serta tempat upacara adat. Dibangun dari kayu ulin yang kuat dan tahan lama, rumah ini juga menggunakan daun nipah atau ijuk sebagai bahan atapnya. Lamin berbentuk panggung dengan tinggi 3 hingga 5 meter dari tanah untuk melindungi dari banjir dan binatang liar, serta membantu sirkulasi udara.</p>
                     <p>Lamin biasanya memiliki panjang hingga 100 meter dan lebar sekitar 15 meter, dengan desain memanjang yang memungkinkan banyak keluarga tinggal bersama. Rumah ini dihiasi dengan ukiran dan motif khas Dayak yang melambangkan kekuatan, keberanian, dan spiritualitas, terutama pada tiang penyangga, dinding, dan pintu. Bagian dalam rumah terdiri dari beberapa ruangan, termasuk ruang utama untuk pertemuan dan upacara, serta ruang tidur yang disekat untuk setiap keluarga. Ada juga ruang penyimpanan hasil panen dan perlengkapan rumah tangga.</p>
                     <p>Rumah adat Dayak Bahau juga berfungsi sebagai pusat upacara adat seperti perayaan gawai, upacara kematian, dan penyambutan tamu, yang semuanya mengikuti ritual dan tata cara khas suku Dayak Bahau. Lamin menjadi pusat kehidupan sosial, tempat anggota komunitas berkumpul, berdiskusi, dan membuat keputusan bersama, mencerminkan nilai-nilai gotong royong dan kebersamaan. Selain itu, rumah ini adalah tempat pendidikan tradisional di mana nilai-nilai, pengetahuan, dan keterampilan diturunkan dari generasi ke generasi.</p>
                     <p>Namun, modernisasi mengakibatkan banyak rumah adat mulai ditinggalkan atau digantikan dengan rumah modern yang lebih praktis dan ekonomis. Upaya pelestarian budaya terus dilakukan oleh komunitas dan pemerintah setempat. Beberapa rumah adat kini menjadi objek wisata yang memperkenalkan kekayaan budaya Dayak Bahau kepada pengunjung dari dalam dan luar negeri. Rumah adat Dayak Bahau bukan hanya simbol kekayaan budaya dan kearifan lokal, tetapi juga bukti keberagaman dan kekayaan tradisi Indonesia yang perlu dijaga dan dilestarikan.</p> </div>
